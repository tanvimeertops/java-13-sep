package com.basic;
/*
 * Arithmetic operator :
 * + -* % /
 */
public class CalcDemo {
public static void main(String[] args) {
	int num1=20,num2=3;
	System.out.println("sum is "+(num1+num2)); // 23
	System.out.println("sub is "+(num1-num2)); //17
	System.out.println("multiplication is "+num1*num2); // 60
	System.out.println("div is "+(float)num1/num2); //6.66
	System.out.println("mod is "+num1%num2);
}
}
